class Song:
    def __init__(self, title, artist, album, lyrics, small_thumb, large_thumb, video_id, user_id):
        self.title = title
        self.artist = artist
        self.album = album
        self.lyrics = lyrics
        self.small_thumb = small_thumb
        self.large_thumb = large_thumb
        self.video_id = video_id
        self.user_id = user_id

